#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - add functions
 * @a: variable one
 * @b: variable two
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction functions
 * @a: variable one
 * @b: variable two
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication functions
 * @a: variable one
 * @b: variable two
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division functions
 * @a: variable one
 * @b: variable two
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - module functions
 * @a: variable one
 * @b: variable two
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
